function switchLanguage(lang) {
    const htmlElement = document.documentElement;
    const langEn = document.getElementById('langEn');
    const langAr = document.getElementById('langAr');

    // Update direction and language attribute
    if (lang === 'ar') {
        htmlElement.setAttribute('dir', 'rtl');
        htmlElement.setAttribute('lang', 'ar');
        if (langAr) langAr.classList.add('active');
        if (langEn) langEn.classList.remove('active');
    } else {
        htmlElement.setAttribute('dir', 'ltr');
        htmlElement.setAttribute('lang', 'en');
        if (langEn) langEn.classList.add('active');
        if (langAr) langAr.classList.remove('active');
    }

    // Update Snapchat Link
    const snapchatLinks = document.querySelectorAll('a[aria-label="Snapchat"]');
    snapchatLinks.forEach(link => {
        if (lang === 'ar') {
            link.href = "https://www.snapchat.com/add/nujoud_build?share_id=R2a4TSnyR5E&locale=ar";
        } else {
            link.href = "https://www.snapchat.com/add/nujoud_build?share_id=R2a4TSnyR5E&locale=en-IN";
        }
    });

    // Translate all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Translate placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });

    localStorage.setItem('preferredLanguage', lang);
}

// Load preferred language on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLanguage') || 'en';
    switchLanguage(savedLang);
});
